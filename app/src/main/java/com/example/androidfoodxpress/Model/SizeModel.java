package com.example.androidfoodxpress.Model;

public class SizeModel {

    private String name;
    private long price;

    public SizeModel() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getPrice() {
        return price;
    }

    public void setPrice(long price) {
        this.price = price;
    }
}
